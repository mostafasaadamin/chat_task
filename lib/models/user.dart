class UserInfoModel {
  final String name;
  final String imageUrl;
  final bool isOnline;
  final String email;
  final String uuid;
  final String lastOnline;

  UserInfoModel({required this.name,
    required  this.imageUrl,
    required  this.isOnline,
    required  this.email,
    required  this.uuid,
    required  this.lastOnline});
}